func TestClineUninstallRemovesOnlyMomFile(t *testing.T) {
	dir := t.TempDir()
	rulesDir := filepath.Join(dir, ".clinerules")
	os.MkdirAll(rulesDir, 0755)

	// Create user file and leo file
	os.WriteFile(filepath.Join(rulesDir, "my-rule.md"), []byte("# Custom"), 0644)
	os.WriteFile(filepath.Join(rulesDir, "mom-context.md"), []byte("<!-- Generated by MOM -->"), 0644)

	a := NewClineAdapter(dir)

	// Simulate uninstall: remove generated files
	for _, f := range a.GeneratedFiles() {
		os.Remove(filepath.Join(dir, f))
	}

	// mom-context.md should be gone
	if _, err := os.Stat(filepath.Join(rulesDir, "mom-context.md")); err == nil {
		t.Error("mom-context.md should have been removed")
	}

	// my-rule.md should still exist
	if _, err := os.Stat(filepath.Join(rulesDir, "my-rule.md")); err != nil {
		t.Error("user file should still exist")
	}

	// .clinerules/ should still exist (GeneratedDirs returns nil)
	if _, err := os.Stat(rulesDir); err != nil {
		t.Error(".clinerules/ directory should still exist")
	}
}
