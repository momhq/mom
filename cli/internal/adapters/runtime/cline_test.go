func TestClineAdapter_GenerateContextFilePreservesDir(t *testing.T) {
	dir := t.TempDir()
	rulesDir := filepath.Join(dir, ".clinerules")
	os.MkdirAll(rulesDir, 0755)

	// Create a user file in .clinerules/
	userFile := filepath.Join(rulesDir, "my-rule.md")
	os.WriteFile(userFile, []byte("# My custom rule"), 0644)

	a := NewClineAdapter(dir)
	config := Config{Version: "1", User: UserConfig{Language: "en", Autonomy: "balanced"}}

	if err := a.GenerateContextFile(config, nil, nil, nil); err != nil {
		t.Fatal(err)
	}

	// User file should still exist
	content, err := os.ReadFile(userFile)
	if err != nil {
		t.Fatal("user file was removed")
	}
	if string(content) != "# My custom rule" {
		t.Error("user file content changed")
	}
}

