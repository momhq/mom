func TestOpenClaudeAdapter_GenerateContextFile(t *testing.T) {
	dir := t.TempDir()
	a := NewOpenClaudeAdapter(dir)

	config := Config{
		Version:  "1",
		Delivery: "context-file",
		User: UserConfig{
			Language:          "en",
			Autonomy:          "balanced",
			CommunicationMode: "concise",
		},
	}

	constraints := []Constraint{
		{ID: "anti-hallucination", Summary: "When unsure, say you don't know."},
	}

	skills := []Skill{
		{ID: "session-wrap-up", Summary: "End-of-session knowledge propagation."},
	}

	identity := &Identity{
		What: "MOM — a living knowledge infrastructure.",
	}

	if err := a.GenerateContextFile(config, constraints, skills, identity); err != nil {
		t.Fatalf("GenerateContextFile failed: %v", err)
	}

	// OpenClaude uses .claude/CLAUDE.md (same as Claude Code).
	content, err := os.ReadFile(filepath.Join(dir, ".claude", "CLAUDE.md"))
	if err != nil {
		t.Fatalf("reading .claude/CLAUDE.md: %v", err)
	}

	s := string(content)

	if !strings.HasPrefix(s, "<!-- Generated by MOM") {
		t.Error("CLAUDE.md should start with watermark")
	}

	checks := []string{
		"Generated by MOM",
		"anti-hallucination",
		"session-wrap-up",
	}
	for _, check := range checks {
		if !strings.Contains(s, check) {
			t.Errorf("CLAUDE.md missing %q", check)
		}
	}
}

func TestOpenClaudeAdapter_RegisterHooks(t *testing.T) {
	dir := t.TempDir()
	a := NewOpenClaudeAdapter(dir)

	if err := a.RegisterHooks(OpenClaudeHooks()); err != nil {
		t.Fatalf("RegisterHooks failed: %v", err)
	}

	// OpenClaude uses .claude/settings.json (same as Claude Code).
	content, err := os.ReadFile(filepath.Join(dir, ".claude", "settings.json"))
	if err != nil {
		t.Fatalf("reading settings.json: %v", err)
	}

	var settings map[string]any
	if err := json.Unmarshal(content, &settings); err != nil {
		t.Fatalf("parsing settings.json: %v", err)
	}

	hooksMap, ok := settings["hooks"].(map[string]any)
	if !ok {
		t.Fatal("hooks should be a map keyed by event name")
	}

	// Verify Stop hooks (record + draft in continuous mode).
	stop, ok := hooksMap["Stop"].([]any)
	if !ok {
		t.Fatal("hooks.Stop should be an array")
	}
	if len(stop) != 2 {
		t.Fatalf("expected 2 Stop matcher groups (record + draft), got %d", len(stop))
	}
	group0 := stop[0].(map[string]any)
	innerHooks0 := group0["hooks"].([]any)
	hookEntry0 := innerHooks0[0].(map[string]any)
	if hookEntry0["command"] != "mom record" {
		t.Errorf("expected command 'mom record', got %v", hookEntry0["command"])
	}
	group1 := stop[1].(map[string]any)
	innerHooks1 := group1["hooks"].([]any)
	hookEntry1 := innerHooks1[0].(map[string]any)
	if hookEntry1["command"] != "mom draft" {
		t.Errorf("expected command 'mom draft', got %v", hookEntry1["command"])
	}

	// SessionEnd should NOT be present — continuous mode uses Stop for both.
	if _, hasSessionEnd := hooksMap["SessionEnd"]; hasSessionEnd {
		t.Error("hooks should NOT have SessionEnd — draft runs on Stop now")
	}
}

func TestOpenClaudeAdapter_RegisterMCP(t *testing.T) {
	dir := t.TempDir()
	a := NewOpenClaudeAdapter(dir)

	if err := a.RegisterMCP(); err != nil {
		t.Fatalf("RegisterMCP failed: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(dir, ".mcp.json"))
	if err != nil {
		t.Fatalf("reading .mcp.json: %v", err)
	}
	s := string(data)
	if !strings.Contains(s, `"mom"`) {
		t.Error(".mcp.json missing mom server entry")
	}
	if !strings.Contains(s, `"command": "mom"`) {
		t.Error(".mcp.json missing mom command")
	}
}

func TestOpenClaudeAdapter_GeneratedFiles(t *testing.T) {
	a := NewOpenClaudeAdapter("/tmp/test")
	files := a.GeneratedFiles()

	expected := []string{
		filepath.Join(".claude", "CLAUDE.md"),
		filepath.Join(".claude", "settings.json"),
		".mcp.json",
	}

	if len(files) != len(expected) {
		t.Fatalf("expected %d files, got %d", len(expected), len(files))
	}
	for i, f := range files {
		if f != expected[i] {
			t.Errorf("file[%d]: expected %q, got %q", i, expected[i], f)
		}
	}
}

func TestOpenClaudeAdapter_GitIgnorePaths(t *testing.T) {
	a := NewOpenClaudeAdapter("/tmp/test")
	paths := a.GitIgnorePaths()

	if len(paths) != 1 || paths[0] != ".claude/" {
		t.Errorf("expected [.claude/], got %v", paths)
	}
}

func TestOpenClaudeAdapter_DetectRuntime(t *testing.T) {
	dir := t.TempDir()
	a := NewOpenClaudeAdapter(dir)

	if a.DetectRuntime() {
		t.Error("expected false when .claude/ does not exist")
	}

	os.MkdirAll(filepath.Join(dir, ".claude"), 0755)
	if !a.DetectRuntime() {
		t.Error("expected true when .claude/ exists")
	}
}

